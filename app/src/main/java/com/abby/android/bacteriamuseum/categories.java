package com.abby.android.bacteriamuseum;


public class categories {

        String exhibitName;
        int exhibitPhoto;

        public categories(String exhibitName, int exhibitPhoto) {
            this.exhibitName = exhibitName;
            this.exhibitPhoto = exhibitPhoto;
        }

        public String getExhibitName() {
            return exhibitName;
        }

        public int getExhibitPhoto() {
            return exhibitPhoto;
        }

}
